void segprint(char *s) {
    for (int i = 0; i < ROWS; ++i) {
        for (char *p = s; *p != '\0'; ++p) {
            int idx = *p - 'A';
            for (int j = 0; j < COLS ; ++j) {
                if (0 <= idx && idx < sizeof(eascii)/sizeof(char*)) {
                    char c = eascii[idx][i*3+j];
                    putchar(c == '_' ? ' ' : c);
                } else {
                    putchar(' ');
                }
            }
            putchar(' ');
        }
        putchar('\n');
    }
}

int main(int argc, char **argv) {
    segprint(argc > 1 ? argv[1] : argv[0]);
    return 0;
}
